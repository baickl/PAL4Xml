
#ifndef _XML_FILE_BRIDGE_H_
#define _XML_FILE_BRIDGE_H_

namespace xnd
{

/*!
 *******************************************************************************
 *	
 *******************************************************************************
 *
 *	\brief		Xml文件操作桥，本类为纯虚类	
 *
 *	\author		陈治国
 *
 *	\version	0.1
 *
 *	\date		2006-07-13
 *
 *	\bug		暂无
 *
 *	\warning	暂无
 *
 ******************************************************************************
 */
class XmlFileBridge
{
public:

	/*!
	 *******************************************************************************
	 *	析构函数
	 *******************************************************************************
	 */
	virtual~XmlFileBridge( void ){}

public:

	/*!
	 *******************************************************************************
	 *	取得文件的长度
	 *******************************************************************************
	 *	\return 返回文件的长度，如果返回-1表示取得失败
	 *******************************************************************************
	 */
	virtual int			length( void ) const			= 0; 

	/*!
	 *******************************************************************************
	 *	取得当前文件位置
	 *******************************************************************************
	 *	\return 返回文件的位置，如果返回-1表示取得失败
	 *******************************************************************************
	 */
	virtual int			getPosition( void ) const		= 0;

	/*!
	 *******************************************************************************
	 *	设置文件当前位置
	 *******************************************************************************
	 *	\param pos 欲设置的位置
	 *******************************************************************************
	 */
	virtual void		setPosition( int pos )			= 0;

	/*!
	 *******************************************************************************
	 *	从文件中读取数据
	 *******************************************************************************
	 */
	virtual bool		read( void* out,int len )		= 0;

	/*!
	 *******************************************************************************
	 *	从文件中写入数据
	 *******************************************************************************
	 */
	virtual bool		write( const void* in,int len ) = 0;

};

}//namespace

#endif//_XML_FILE_BRIDGE_H_